    // This Add Button for Internal Sektion.
    private void BTNAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNAddActionPerformed
        if (TXTName.getText().equals("") || TXTSSN.getText().equals("") || TXTChair.getText().equals(""))
        {
            Tools.myMess("من فضلك املئ كل البيانات");
        }
        else
        {
            in.setOffer_code(Integer.parseInt(TXTCode.getText()));
            if (in.count() == 47)
            {
                Tools.myMess("تمت حجز الرحلة بالكامل");
                int x = JOptionPane.showConfirmDialog(null, "هل تريد طباعة كشف الرحلة؟", "تأكيد", JOptionPane.YES_NO_OPTION);
                if (x == 0)
                {
                    Tools.openForm(new FlightStatement());
                    this.dispose();
                }
            }
            else
            {
                ac.setName(TXTName.getText());
                ac.setSSN(Long.parseLong(TXTSSN.getText()));
                ac.setTele(TXTTele.getText());
                ac.setOffer_code(Integer.parseInt(TXTCode.getText()));
                ac.setSeate(Integer.parseInt(TXTChair.getText()));
                ac.setPSSN1(Long.parseLong(TXTParent.getText()));

                boolean a = ac.quary();

                if (a == true)
                {
                    Tools.myMess("هذا الكرسى تم حجزه بالفعل");
                    TXTChair.setText("");
                    TXTChair.requestFocus();
                }
                else
                {
                    ac.add();
                    ac.getOneRow(InternalAdd, "Accompanying", 0, 0);
                }
            }
            
        }
    }//GEN-LAST:event_BTNAddActionPerformed

