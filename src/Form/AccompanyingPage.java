    // This Print Resete Button for Umrah Sektion.
    private void BTNPrint1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNPrint1ActionPerformed
        u.setOffer_code(Integer.parseInt(TXTOffer1.getText()));
        int price = Integer.parseInt(u.Quary());
        int count = Integer.parseInt(ac.countU()) + 1;
        
        // get the final cash in gui form.
        UmrahSektion.TXTCash.setText(String.valueOf(price*count));
        
        String path = null;
        JFileChooser ch = new JFileChooser();
        ch.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int y = ch.showSaveDialog(this);
        if (y == JFileChooser.APPROVE_OPTION)
        {
            path = ch.getSelectedFile().toString();
        }
        try {
            setModel1();
            PrintWriter ws = new PrintWriter(path+".txt");
            ws.println(" ---------------- The final coast you will cash it -------------------- ");
            ws.println("Person number 1  : "+UmrahSektion.TXTName.getText() + "    " +" his price "+price);
            for (int i=0 ; i< Model.getRowCount() ; i++)
            {
                ws.println("Person number "+ i+2 +" : "+ Model.getValueAt(i, 0) + "    " +" his price "+price);
            }
            ws.println("-------------------------------------------------------------------------");
            // get the final cash in resete text.
            ws.println(" The Result is "+ Integer.parseInt(UmrahSektion.TXTCash.getText()));
            ws.println(" The Resete was written by "+in.getEmp_name() + " and");
            ws.println(" It was printed in "+Tools.PrintDate());
            ws.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(InternalSektion.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_BTNPrint1ActionPerformed

